// && Logical Operator is true when the left and right terms are both true.

if (true && true) {
  console.log(1); //Print 1
}

if (true && false) {
  console.log(2); //Print Nothing
}

if (false && true) {
  console.log(3); //Print Nothing
}

if (false && false) {
  console.log(4); //Print Nothing
}
